"""
Wheel model object
"""

from .body import Body
from support.tuple_math import TupleMath

class Wheel(Body):
    """
    Wheel model object
    """

    def __init__(self, center, width=0.8333, diameter=1.75):
        """
        Constructor
        """

        self.width = width
        self.diameter = diameter
        self.angle = 0.0
        self.center = center

        super().__init__([diameter, width], self.center)
